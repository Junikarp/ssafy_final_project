package com.bremen.model.dto;

public class Trade {
	private int tradeId;
	private String tradeTitle;
	private String tradeContent;
	private String tradeWriter;
	private int tradeViewCnt;	
	private String tradeDate;
	private String tradePlace;
	private int tradePrice;
	private String tradeCategory;
	
	public int getTradeId() {
		return tradeId;
	}
	public void setTradeId(int tradeId) {
		this.tradeId = tradeId;
	}
	public String getTradeTitle() {
		return tradeTitle;
	}
	public void setTradeTitle(String tradeTitle) {
		this.tradeTitle = tradeTitle;
	}
	public String getTradeContent() {
		return tradeContent;
	}
	public void setTradeContent(String tradeContent) {
		this.tradeContent = tradeContent;
	}
	public String getTradeWriter() {
		return tradeWriter;
	}
	public void setTradeWriter(String tradeWriter) {
		this.tradeWriter = tradeWriter;
	}
	public int getTradeViewCnt() {
		return tradeViewCnt;
	}
	public void setTradeViewCnt(int tradeViewCnt) {
		this.tradeViewCnt = tradeViewCnt;
	}
	public String getTradeDate() {
		return tradeDate;
	}
	public void setTradeDate(String tradeDate) {
		this.tradeDate = tradeDate;
	}
	public String getTradePlace() {
		return tradePlace;
	}
	public void setTradePlace(String tradePlace) {
		this.tradePlace = tradePlace;
	}
	public int getTradePrice() {
		return tradePrice;
	}
	public void setTradePrice(int tradePrice) {
		this.tradePrice = tradePrice;
	}
	public String getTradeCategory() {
		return tradeCategory;
	}
	public void setTradeCategory(String tradeCategory) {
		this.tradeCategory = tradeCategory;
	}
}
